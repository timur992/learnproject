package com.qaforall.learnauto;

public class Calculator {

	public int sumOfTwoNumbers(int numberOne, int numberTwo) {
		int sum =numberOne + numberTwo;
		return sum;
	}
	
	public int sumOfThreeNumbers(int numberOne, int numberTwo, int numberThree) {
		int sum =numberOne + numberTwo + numberThree;
		return sum;
	}

}
